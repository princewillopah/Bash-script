# Automatically sets up a backup directory with today's date
name="Princewill"
env="dev"
count=5

BACKUP_DIR="/backups/$(date +%Y-%m-%d)"
mkdir -p "$BACKUP_DIR"
echo "Backup directory created: $BACKUP_DIR"

### ==========================================================================================================================================================
### Dynamic File Naming with Variables
### ==========================================================================================================================================================
# Use case: Automated NGINX backup with time-stamped file names.
timestamp=$(date +%F-%H%M%S)
backup_file="backup-${timestamp}.tar.gz"
tar -czf "$backup_file" /etc/nginx




### ==========================================================================================================================================================
### Docker Tagging Based on Git Commit
### ==========================================================================================================================================================
# Use case: CI pipeline tags images with the current Git commit.
GIT_COMMIT=$(git rev-parse --short HEAD)
docker build -t my-app:$GIT_COMMIT .



### ==========================================================================================================================================================
###  Secrets with Defaults
### ==========================================================================================================================================================
# Use case: Use environment variable if set; fallback to default for local/dev environments.
DB_PASSWORD=${DB_PASSWORD:-"defaultpassword"}




### ==========================================================================================================================================================
### Loop Through a List of Servers
### ==========================================================================================================================================================
# Use case: Remote health check on a fleet of servers.
servers=("web1" "web2" "db1")
for server in "${servers[@]}"; do
  echo "Checking uptime on $server"
  ssh "$server" uptime
done

### ==========================================================================================================================================================
###  Logging with Timestamps
### ==========================================================================================================================================================
# Use case: Maintain deployment logs with timestamps
log_file="deploy.log"
log() {
  echo "$(date +%F\ %T) - $1" >> $log_file
}

log "Starting deployment"




